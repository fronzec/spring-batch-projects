-- =========================================================
-- Spring Batch schema for H2 (in-memory test database)
-- Source: spring-batch-core schema-h2.sql
-- =========================================================

CREATE TABLE IF NOT EXISTS BATCH_JOB_INSTANCE (
    JOB_INSTANCE_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    VERSION BIGINT,
    JOB_NAME VARCHAR(100) NOT NULL,
    JOB_KEY VARCHAR(32) NOT NULL,
    constraint JOB_INST_UN unique (JOB_NAME, JOB_KEY)
);

CREATE TABLE IF NOT EXISTS BATCH_JOB_EXECUTION (
    JOB_EXECUTION_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    VERSION BIGINT,
    JOB_INSTANCE_ID BIGINT NOT NULL,
    CREATE_TIME TIMESTAMP(9) NOT NULL,
    START_TIME TIMESTAMP(9) DEFAULT NULL,
    END_TIME TIMESTAMP(9) DEFAULT NULL,
    STATUS VARCHAR(10),
    EXIT_CODE VARCHAR(2500),
    EXIT_MESSAGE VARCHAR(2500),
    LAST_UPDATED TIMESTAMP(9),
    constraint JOB_INST_EXEC_FK foreign key (JOB_INSTANCE_ID)
    references BATCH_JOB_INSTANCE(JOB_INSTANCE_ID)
);

CREATE TABLE IF NOT EXISTS BATCH_JOB_EXECUTION_PARAMS (
    JOB_EXECUTION_ID BIGINT NOT NULL,
    PARAMETER_NAME VARCHAR(100) NOT NULL,
    PARAMETER_TYPE VARCHAR(100) NOT NULL,
    PARAMETER_VALUE VARCHAR(2500),
    IDENTIFYING CHAR(1) NOT NULL,
    constraint JOB_EXEC_PARAMS_FK foreign key (JOB_EXECUTION_ID)
    references BATCH_JOB_EXECUTION(JOB_EXECUTION_ID)
);

CREATE TABLE IF NOT EXISTS BATCH_STEP_EXECUTION (
    STEP_EXECUTION_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    VERSION BIGINT NOT NULL,
    STEP_NAME VARCHAR(100) NOT NULL,
    JOB_EXECUTION_ID BIGINT NOT NULL,
    CREATE_TIME TIMESTAMP(9) NOT NULL,
    START_TIME TIMESTAMP(9) DEFAULT NULL,
    END_TIME TIMESTAMP(9) DEFAULT NULL,
    STATUS VARCHAR(10),
    COMMIT_COUNT BIGINT,
    READ_COUNT BIGINT,
    FILTER_COUNT BIGINT,
    WRITE_COUNT BIGINT,
    READ_SKIP_COUNT BIGINT,
    WRITE_SKIP_COUNT BIGINT,
    PROCESS_SKIP_COUNT BIGINT,
    ROLLBACK_COUNT BIGINT,
    EXIT_CODE VARCHAR(2500),
    EXIT_MESSAGE VARCHAR(2500),
    LAST_UPDATED TIMESTAMP(9),
    constraint JOB_EXEC_STEP_FK foreign key (JOB_EXECUTION_ID)
    references BATCH_JOB_EXECUTION(JOB_EXECUTION_ID)
);

CREATE TABLE IF NOT EXISTS BATCH_STEP_EXECUTION_CONTEXT (
    STEP_EXECUTION_ID BIGINT NOT NULL PRIMARY KEY,
    SHORT_CONTEXT VARCHAR(2500) NOT NULL,
    SERIALIZED_CONTEXT LONGVARCHAR,
    constraint STEP_EXEC_CTX_FK foreign key (STEP_EXECUTION_ID)
    references BATCH_STEP_EXECUTION(STEP_EXECUTION_ID)
);

CREATE TABLE IF NOT EXISTS BATCH_JOB_EXECUTION_CONTEXT (
    JOB_EXECUTION_ID BIGINT NOT NULL PRIMARY KEY,
    SHORT_CONTEXT VARCHAR(2500) NOT NULL,
    SERIALIZED_CONTEXT LONGVARCHAR,
    constraint JOB_EXEC_CTX_FK foreign key (JOB_EXECUTION_ID)
    references BATCH_JOB_EXECUTION(JOB_EXECUTION_ID)
);

CREATE SEQUENCE IF NOT EXISTS BATCH_STEP_EXECUTION_SEQ;
CREATE SEQUENCE IF NOT EXISTS BATCH_JOB_EXECUTION_SEQ;
CREATE SEQUENCE IF NOT EXISTS BATCH_JOB_INSTANCE_SEQ;

-- =========================================================
-- Application-specific tables
-- =========================================================

CREATE TABLE IF NOT EXISTS job_definitions (
    id BIGINT PRIMARY KEY AUTO_INCREMENT,
    job_name VARCHAR(255) UNIQUE NOT NULL,
    display_name VARCHAR(255),
    description TEXT,
    version VARCHAR(50) NOT NULL,
    jar_file_path VARCHAR(500) NOT NULL,
    jar_checksum VARCHAR(64) NOT NULL,
    main_class_name VARCHAR(255) NOT NULL,
    enabled BOOLEAN DEFAULT true,
    auto_start BOOLEAN DEFAULT false,
    load_status VARCHAR(50),
    load_error TEXT,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    created_by VARCHAR(100),
    approval_status VARCHAR(20) DEFAULT 'PENDING' NOT NULL,
    approved_by VARCHAR(100),
    approved_at TIMESTAMP NULL
);

CREATE INDEX IF NOT EXISTS idx_job_name ON job_definitions (job_name);
CREATE INDEX IF NOT EXISTS idx_enabled ON job_definitions (enabled);
CREATE INDEX IF NOT EXISTS idx_load_status ON job_definitions (load_status);

CREATE TABLE IF NOT EXISTS job_parameters_template (
    id BIGINT PRIMARY KEY AUTO_INCREMENT,
    job_definition_id BIGINT NOT NULL,
    param_key VARCHAR(100) NOT NULL,
    param_type VARCHAR(50) NOT NULL,
    default_value VARCHAR(255),
    required BOOLEAN DEFAULT false,
    description TEXT,
    validation_regex VARCHAR(500),
    CONSTRAINT fk_job_parameters_template_job_definition_id
        FOREIGN KEY (job_definition_id)
        REFERENCES job_definitions(id)
        ON DELETE CASCADE,
    CONSTRAINT uk_job_param UNIQUE (job_definition_id, param_key)
);

CREATE TABLE IF NOT EXISTS job_executions_audit (
    id BIGINT PRIMARY KEY AUTO_INCREMENT,
    job_definition_id BIGINT NOT NULL,
    job_execution_id BIGINT NOT NULL,
    job_version VARCHAR(50),
    execution_metadata JSON,
    CONSTRAINT fk_job_executions_audit_job_definition_id
        FOREIGN KEY (job_definition_id)
        REFERENCES job_definitions(id)
);

CREATE TABLE IF NOT EXISTS persons (
    id         BIGINT PRIMARY KEY AUTO_INCREMENT,
    first_name VARCHAR(50)  NOT NULL,
    last_name  VARCHAR(50),
    profession VARCHAR(30)  NOT NULL,
    email      VARCHAR(50)  NOT NULL,
    processed  BOOLEAN      NOT NULL DEFAULT false,
    created_at TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP    NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS persons_v2 (
    id                     BIGINT PRIMARY KEY AUTO_INCREMENT,
    snapshot_date          DATE          NOT NULL,
    first_name             VARCHAR(50)   NOT NULL,
    last_name              VARCHAR(50)   NOT NULL,
    email                  VARCHAR(50)   NOT NULL,
    profession             VARCHAR(15)   NOT NULL,
    salary                 DECIMAL(19,2) NOT NULL,
    uuid_v4                VARCHAR(36)   NOT NULL,
    created_at             TIMESTAMP     NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at             TIMESTAMP     NOT NULL DEFAULT CURRENT_TIMESTAMP,
    fk_dispatched_group_id BIGINT        NULL
);

CREATE TABLE IF NOT EXISTS dispatched_group (
    id               BIGINT PRIMARY KEY AUTO_INCREMENT,
    uuid_v4          VARCHAR(36) NOT NULL,
    dispatch_status  VARCHAR(10) NOT NULL DEFAULT 'UNKNOWN',
    records_included INT         NOT NULL DEFAULT 0,
    created_at       TIMESTAMP   NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at       TIMESTAMP   NOT NULL DEFAULT CURRENT_TIMESTAMP
);

-- =========================================================
-- V5: Ticket PDF tables (mirror of V5__ticket_pdf_tables.sql)
-- =========================================================

CREATE TABLE IF NOT EXISTS event_tickets (
    id             BIGINT        PRIMARY KEY AUTO_INCREMENT,
    event_id       BIGINT        NOT NULL,
    ticket_code    VARCHAR(64)   NOT NULL,
    holder_name    VARCHAR(255)  NOT NULL,
    event_name     VARCHAR(255)  NOT NULL,
    event_location VARCHAR(255)  NULL,
    seat           VARCHAR(64)   NULL,
    event_datetime TIMESTAMP     NOT NULL,
    processed      BOOLEAN       NOT NULL DEFAULT FALSE,
    created_at     TIMESTAMP     NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT uk_event_tickets_ticket_code UNIQUE (ticket_code)
);

CREATE INDEX IF NOT EXISTS idx_event_tickets_processed ON event_tickets (processed);

CREATE TABLE IF NOT EXISTS generated_files (
    id              BIGINT        PRIMARY KEY AUTO_INCREMENT,
    ticket_id       BIGINT        NOT NULL,
    storage_type    VARCHAR(16)   NOT NULL DEFAULT 'LOCAL',
    storage_path    VARCHAR(1024) NOT NULL,
    checksum_sha256 CHAR(64)      NOT NULL,
    file_size_bytes BIGINT        NOT NULL,
    generated_at    TIMESTAMP     NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_generated_files_ticket
        FOREIGN KEY (ticket_id) REFERENCES event_tickets (id),
    CONSTRAINT uk_generated_files_ticket UNIQUE (ticket_id)
);

-- =========================================================
-- V6: Ticket bundle tables (mirror of V6__ticket_bundle_tables.sql)
-- =========================================================

CREATE TABLE IF NOT EXISTS generated_bundles (
    id              BIGINT        PRIMARY KEY AUTO_INCREMENT,
    event_id        BIGINT        NOT NULL,
    storage_type    VARCHAR(16)   NOT NULL DEFAULT 'LOCAL',
    storage_path    VARCHAR(1024) NOT NULL,
    checksum_sha256 CHAR(64)      NOT NULL,
    file_size_bytes BIGINT        NOT NULL,
    ticket_count    INT           NOT NULL,
    status          VARCHAR(20)   NOT NULL DEFAULT 'COMPLETED',
    created_at      TIMESTAMP     NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT uk_generated_bundles_event UNIQUE (event_id)
);
